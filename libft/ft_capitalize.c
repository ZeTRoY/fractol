#include "libft.h"

void		ft_capitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] < 'A' || (str[i] > 'Z' && str[i] < 'a') || str[i] > 'z')
		i++;
	if (str[i] >= 'a' && str[i] <= 'z')
		str[i] -= 32;
}